## code to prepare `sweden_1993` dataset goes here
sweden_1993 <- function() {
    P0F <- P0M <-
        c(293395,248369,240012,261346,285209,314388,281290,286923,
          304108,324946,247613,211351,215140,221764,223506,183654,
          141990,112424)
    LxF <- LxM <- c(497487,497138,496901,496531,495902,495168,494213,492760,
             490447,486613,480665,471786,457852,436153,402775,350358,
             271512,291707)
    Fx <- c(0.0120,0.0908,0.1499,0.1125,0.0441,0.0074,0.0003) #not saved
    NMxF <- NMxM <- c(6840,4150,3365,5270,9240,8230,5470,3155,1770,1115,1075,
              845,645,530,465,300,250,175)
    SxF <- LxF/c(NA, head(LxF, -1))
    SxF[1] <- LxF[1]/500000
    SxF[18] <- LxF[18]/(sum(LxF[17:18]))
    SxM <- SxF
    asfr <- c(rep(0,3), Fx, rep(0,8))
    srb <- 1.05
    n <- 5

    PzF <- c(302392, 298682, 252007, 244150, 268267, 293515, 320624,
          284767, 288031, 303166, 322062, 243989, 205841, 205516,
          205270, 194771, 142565, 131966)
    PzM <- c(317340, 298682, 252007, 244150, 268267, 293515, 320624,
          284767, 288031, 303166, 322062, 243989, 205841, 205516,
          205270, 194771, 142565, 131966)

    return(list(P0F = P0F, P0M = P0M, LxF = LxF, LxM = LxM,
                NMxF = NMxF, NMxM = NMxM, SxF = SxF, SxM = SxM,
                asfr = asfr, srb = srb, n = n,
                PzF = PzF, PzM = PzM))
}

## Overwrites function
sweden_1993 <- sweden_1993()

## Creates the data .rda file
usethis::use_data(sweden_1993, overwrite = TRUE)
